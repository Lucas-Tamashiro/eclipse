package mod02Estruturas;

public class EFor {
	public static void main(String[] args) {
		
		// For looping = Executa um bloco de código por um número limitado de vezes.
		for (int i=1; i<=10; i++) { // Conta de 1 a 10 
			System.out.println(i);
		}
		
		System.out.println("-----------------------------------------------");
		
		for (int i=10; i >= 1; i--) { // i = index, índice, 
			System.out.println(i);
		}
		System.out.println("Feliz ano novo!");
	}
}
